#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	int				str_len;
	char			*res;

	if (!s || !f)
		return (NULL);
	i = 0;
	str_len = ft_strlen(s);
	res = malloc(str_len + 1);
	if (res == NULL)
		return (NULL);
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
